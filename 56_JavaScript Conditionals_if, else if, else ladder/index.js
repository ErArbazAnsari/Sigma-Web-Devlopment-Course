//Operator in Javascript
// +, -, *, /, **, %, ++, --
let num1 = 2;
let num2 = 3;

console.log("+ ", num1 + num2);
console.log("- ", num1 - num2);
console.log("* ", num1 * num2);
console.log("/ ", num1 / num2);
console.log("** ", num1 ** num2);
console.log("% ", num1 % num2);
console.log("++ ", ++num1);
console.log("-- ", ++num1);

